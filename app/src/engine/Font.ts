export function drawString(engine: PixelEngine, text: string, startX: number, startY: number) {
  let currX = Math.floor(startX);
  const currY = Math.floor(startY);
  
  for (let i = 0; i < text.length; i++) {
    const char = text[i];
    const bitmap = Font5x7[char] || Font5x7[' '];

    // Decode the 5x7 bitmap onto the engine grid
    for (let row = 0; row < 7; row++) {
      const rowBits = bitmap[row];
      for (let col = 0; col < 5; col++) {
        // Check if the bit at this column is 1
        const mask = 1 << (4 - col);
        if ((rowBits & mask) !== 0) {
          engine.drawPixel(currX + col, currY + row);
        }
      }
    }

    // Move to next character. 5 pixels wide + 1 pixel spacing = 6.
    currX += 6;
  }
}
